<P> (CNN Español) -- Finalmente, Lionel Messi no jugará en el encuentro frente a Atlanta United este sábado. El argentino ni siquiera viajó con sus compañeros y se quedó en Miami de cara a un calendario ajustado que tiene el equipo dirigido por Gerardo Martino.


El 10 arrastra una molestia física desde el partido frente a Ecuador por las eliminatorias sudamericanas que no le permitió siquiera ir al banco por la segunda fecha de la competencia frente a Bolivia.

Si bien regresó rápido a Miami para ponerse a las órdenes de Martino, el mismo entrenador en conferencia advirtió que el equipo tiene muchos partidos en poco tiempo y eso iba a ser un factor a la hora de tomar una decisión con respecto al 10 y el encuentro ante Atlanta.

El conjunto de Miami afrontará su segundo partido sin Messi desde su llegada. El anterior fue una victoria por 3 a 2 ante Kansas City, mientras el ex PSG se encontraba con la selección argentina por las eliminatorias. Con 8 partidos por jugar, las Garzas están a 6 puntos del DC United, que tiene en su poder, por el momento, la última plaza para ingresar a los playoffs.
El último antecedente de Inter Miami frente al Atlanta United es del 27 de julio, cuando el equipo del sur de Florida se impuso 4 a 0 con dos goles de Messi.

El rival: Atlanta United FC
El club de Georgia marcha sexto en la Conferencia Este con 42 puntos y se ubica en zona de playoffs. Si bien es un equipo que se hace fuerte jugando de local en el Mercedes-Benz Stadium (ganó 8, empató 3 y perdió 3) de los últimos 3 encuentros que jugó ahí perdió 2 y ganó 1.

El argentino Thiago Almada es la figura del conjunto dirigido por el mexicano Gonzalo Pineda. El campeón del mundo con Argentina contribuyó al equipo con 9 goles y 14 asistencia, más que cualquiera de sus compañeros.

